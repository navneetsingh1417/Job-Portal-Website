package employee;

public class Employee_Bean {

	private String email;
	private String phone;
	private String password;
	private String fullname;
	private String gender;
	private String dob;
	private String high_qualif;
	private String marks_12;
	private String marks_10;
	private String field_of_interest;
	private String photo;
	
	
	public String getFullname() {
		return fullname;
	}
	public void setFullname(String fullname) {
		this.fullname = fullname;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getDob() {
		return dob;
	}
	public void setDob(String dob) {
		this.dob = dob;
	}
	public String getHigh_qualif() {
		return high_qualif;
	}
	public void setHigh_qualif(String high_qualif) {
		this.high_qualif = high_qualif;
	}
	public String getMarks_12() {
		return marks_12;
	}
	public void setMarks_12(String marks_12) {
		this.marks_12 = marks_12;
	}
	public String getMarks_10() {
		return marks_10;
	}
	public void setMarks_10(String marks_10) {
		this.marks_10 = marks_10;
	}
	public String getField_of_interest() {
		return field_of_interest;
	}
	public void setField_of_interest(String field_of_interest) {
		this.field_of_interest = field_of_interest;
	}
	
	

	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getPhoto() {
		return photo;
	}
	public void setPhoto(String photo) {
		this.photo = photo;
	}
	
}
